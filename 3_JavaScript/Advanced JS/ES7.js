// ES7 launched just two features:
// .includes() method
// ** operator (exponentiation)
console.log('Hello'.includes('o'))

const pets = ['cat', 'dog', 'bat']
console.log(pets.includes('dog'))
console.log(pets.includes('bird'))


const square = (x) => x ** 2;
console.log(square(2))

const cube = (y) => y ** 3;
console.log(cube(3))
